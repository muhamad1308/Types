a = float(input("Enter number 1 (decimal number) -> ")) #a variable(float number eg: 12.12)
b = float(input("Enter number 1 (decimal number) -> ")) #b variable(float number eg: 6.6)
total= a+b #sum of two variables

subtraction = a-b

aggrandizement = a*b #the product of two numbers

division = a/b #division of two numbers

degree_a = a**2 #the square of a

degree_b = b**2 #the square of b

print(f"{a} + {b} = {total} \n{a} - {b} = {subtraction} \n{a} * {b} = {aggrandizement} \n{a} / {b} = {division} \n{a}^2 = {degree_a}; {b}^2 = {degree_b}") #print all actions

